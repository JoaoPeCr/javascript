var agora = new Date()
var DiaDaSemana = agora.getDay()
switch (DiaDaSemana){
  case 0:
    console.log(`Domingo`);
    break
  case 0:
    console.log(`Segunda-feira`);
    break
  case 0:
    console.log(`Terça-feira`);
    break
  case 0:
    console.log(`Quarta-feira`);
    break
  case 0:
    console.log(`Quinta-feira`);
    break
  case 0:
    console.log(`Sexta-feira`);
    break
  case 0:
    console.log(`Sábado`);
    break
  default: 
  console.log(`[ERRO] Dia inválido!`)
    break
}
